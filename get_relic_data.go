const market_url string = "https://api.warframe.market/v1/items/"
const relic_url string = "https://drops.warframestat.us/data/relics.json"

type AutoGenerated struct {
	Relics []struct {
		Tier      string `json:"tier"`
		RelicName string `json:"relicName"`
		State     string `json:"state"`
		Rewards   []struct {
			ID       string `json:"_id"`
			ItemName string `json:"itemName"`
			Rarity   string `json:"rarity"`
			Chance   int    `json:"chance"`
		} `json:"rewards"`
		ID string `json:"_id"`
	} `json:"relics"`
}

func getRelics(mongourl string) {
	resp, err := http.Get(relic_url)
	if err != nil {
		log.Fatal(err)
	}
	defer resp.Body.Close()
	body, readerr := ioutil.ReadAll(resp.Body)
	fmt.Println("3")
	if readerr != nil {
		log.Fatal(readerr)
	}
	fmt.Println(resp)
	var relicPage AutoGenerated

	jsonerr := json.Unmarshal(body, &relicPage)
	fmt.Println("4")
	if jsonerr != nil {
		log.Fatal(jsonerr)
		//return err
	}
	//client, _ := mongo.NewClient(options.Client().ApplyURI("mongodb://localhost:27017"))
	client, _ := mongo.NewClient(options.Client().ApplyURI(mongourl))
	ctx, _ := context.WithTimeout(context.Background(), 10*time.Second)
	client.Connect(ctx)
	r_coll := client.Database("warframe").Collection("relics")
	i_coll := client.Database("warframe").Collection("items")
	for _, relic := range relicPage.Relics {
		r_coll.Indexes()
		fmt.Print(relic)
		i_coll.Indexes()
	}
}

func main() {
	if len(os.Args) != 2 {
		log.Fatalln("Usage: get_relic_data [mongo url]")
	}
	mongoURL, err := ioutil.ReadFile(os.Args[1])
	if err != nil {
		log.Fatalln("Argument should be a file with the URL of your mongodb server")
	}
	getRelics(string(mongoURL))
}
